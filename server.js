function normalise(str) {
  return (str || '')
    .trim()
    .replace(/[\u064B-\u065F\u0670]/g, '')   // تشكيل
    .replace(/[أإآا]/g, 'ا')                  // همزات
    .replace(/ة/g, 'ه')                       // تاء مربوطة
    .replace(/ى/g, 'ي')                       // ألف مقصورة
    .replace(/\s+/g, ' ')
    .toLowerCase();
}

function calcPoints(mode, timeTaken, hintsUsed) {
  const base = mode === 'verse' ? 20 : (mode === 'who' ? 12 : 10);
  const speedBonus = Math.max(0, Math.floor(base - timeTaken));
  const hintPenalty = hintsUsed > 2 ? (hintsUsed - 2) * 4 : 0;
  return Math.max(1, base + speedBonus - hintPenalty);
}

