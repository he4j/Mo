app.post('/api/ai-ask', async (req, res) => {
  const { question } = req.body;
  if (!question) return res.json({ answer: 'يرجى كتابة سؤالك.' });

  // بحث محلي أولاً
  const q = question.toLowerCase();
  for (const [name, info] of Object.entries(POETS_DB)) {
    if (q.includes(name.toLowerCase()) || q.includes(name.split(' ')[0].toLowerCase())) {
      const answer = buildPoetAnswer(name, info, q);
      if (answer) return res.json({ answer });
    }
  }

  // ردود عامة عن الشعر
  if (q.includes('عصر') || q.includes('حقبة')) {
    const eras = [...new Set(Object.values(POETS_DB).map(p => p.era))];
    return res.json({ answer: `عصور الشعر العربي الرئيسية: ${eras.join('، ')}. كل عصر له خصائصه الفنية المميزة.` });
  }

  return res.json({
    answer: `سؤال رائع! لدي في قاعدتي معلومات عن: ${Object.keys(POETS_DB).join('، ')}. اسألني عن أي منهم!`
  });
});

function buildPoetAnswer(name, info, question) {
  if (question.includes('ولد') || question.includes('مولد') || question.includes('ميلاد')) {
    return `وُلد ${name} في: ${info.born}`;
  }
  if (question.includes('مات') || question.includes('توفي') || question.includes('وفاة')) {
    return `توفي ${name}: ${info.died}`;
  }
  if (question.includes('عصر') || question.includes('حقبة')) {
    return `ينتمي ${name} إلى ${info.era}.`;
  }
  if (question.includes('أشهر') || question.includes('قصيدة') || question.includes('بيت')) {
    return `من أشهر أبيات ${name}:\n• ${info.famous.join('\n• ')}`;
  }
  if (question.includes('لقب') || question.includes('كنية')) {
    return `لقب ${name}: "${info.nickname}"`;
  }
  if (question.includes('أسلوب') || question.includes('شعر') || question.includes('موضوع')) {
    return `اشتهر ${name} بـ: ${info.style}`;
  }
  // معلومات عامة
  return `${name} (${info.nickname})\n📅 ${info.born}\n💀 ${info.died}\n📚 ${info.era}\n🖊️ ${info.style}\n\n${info.bio}`;
}

