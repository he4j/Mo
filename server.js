    const { roomCode, playerName, playerAge } = data;
    const room = rooms[roomCode];
    if (room && room.players.length < 2) {
      room.players.push({
        id:         socket.id,
        name:       (playerName || 'لاعب').substring(0, 30),
        age:        playerAge || 0,
        score:      0,
        hintsUsed:  0
      });
      socket.join(roomCode);
      if (room.players.length === 2) io.to(roomCode).emit('playersReady', room.hostId);
    } else {
      socket.emit('errorMsg', 'المجلس ممتلئ أو الرمز خطأ!');
    }
