function buildHints(answer, poet, context) {
  const a = answer.trim();
  const len = a.length;
  const firstChar = a[0] || '؟';
  const lastChar = a[a.length - 1] || '؟';
  const midChar = len > 2 ? a[Math.floor(len / 2)] : '';
  const poetInfo = POETS_DB[poet] || {};
  const era = poetInfo.era || 'غير محدد';
  const style = poetInfo.style || '';

  return [
    // تلميح 1: بنيوي خفيف
    `الإجابة تتكون من ${len > 1 ? len + ' أحرف' : 'حرف واحد'} وتبدأ بحرف "${firstChar}".`,
    // تلميح 2: دلالي
    context || `الكلمة تنتمي لسياق ${style || 'شعري'} من العصر ${era}.`,
    // تلميح 3: حرف أخير
    `الإجابة تنتهي بحرف "${lastChar}"${midChar ? ' وفي وسطها "${midChar}"' : ''}.`,
    // تلميح 4: عن الشاعر
    poetInfo.bio
      ? `تلميح من سيرة الشاعر: ${poetInfo.bio.split('.')[0]}.`
      : `هذا البيت للشاعر ${poet} من أبرز شعراء ${era}.`,
    // تلميح 5: شبه مباشر
    `الإجابة: ${a[0]}${'_'.repeat(Math.max(0, len - 2))}${len > 1 ? a[a.length - 1] : ''} — تأمّل جيداً!`
  ];
}

